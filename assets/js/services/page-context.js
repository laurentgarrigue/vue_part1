/**
 * Return the current category id that's set by the server
 *
 * @returns {string|null}
 */
export function getCurrentCategoryId() {
    return window.currentCategoryId;
}

/**
 * Return the current product id that's set by the server
 *
 * @returns {string|null}
 */
export function getCurrentProductId() {
    return window.currentProductId;
}
